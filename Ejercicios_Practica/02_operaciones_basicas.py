# Pida al usuario ingresar dos números y muestre el resultado de las operaciones aritméticas básicas. (suma, resta, multiplicación, división).

a = float(input("Ingrese el primer número: "))
b = float(input("Ingrese el segundo número: "))

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b

print("La suma es: ", suma, "\nLa resta es:", resta, "\nLa multiplicación es: ", multiplicacion, "\nLa división es: ", division)